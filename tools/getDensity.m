				function ci = bootCI_calc(vect, nboot, fhandle, alpha)
					mm = fhandle(vect);
					xci = bootci(nboot,{fhandle,vect},'alpha',alpha);
					ci = xci;
					ci = [xci(1) mm xci(2)];
					ci = diff(ci);
					ci = max(ci);
