						youttext=sprintf('%0.3g ',ycol(idx2>0)');
						xcol(idx1>0)=[];
						ycol(idx2>0)=[];
					end
				case '3SD'
					mtmp=repmat(xmean,length(xcol),1);
					stmp=repmat(xstd,length(xcol),1);
					idx1=abs(xcol-mtmp)>3*stmp;
					
