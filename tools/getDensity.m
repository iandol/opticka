		function scatterColumns(obj,c1,c2)
			
			obs1 = obj.xdata.Properties.VarNames{c1};
			obs2 = obj.xdata.Properties.VarNames{c2};
			
			d1 = obj.xdata.(obs1);
			d2 = obj.xdata.(obs2);
